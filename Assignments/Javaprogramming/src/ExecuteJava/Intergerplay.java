package ExecuteJava;

public class Intergerplay {

	public static void main(String[] args) {
		// Add two numbers 
		
		int a = 100;
		int b = 200;
		int c = a+b;
		int d = a-b;
		int e = a*b;
		int f = a/b;
		int g = a%b;
		
		System.out.println("Sum of a+b: " + c);
		System.out.println("Subtract of a-b: " + d);
		System.out.println("Multiply of a*b: " + e);
		System.out.println("Division of a/b: " + f);
		System.out.println("Reminder of a%b: " + g);

	}

}
