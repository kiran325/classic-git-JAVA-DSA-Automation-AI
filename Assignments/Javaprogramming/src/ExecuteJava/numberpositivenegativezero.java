package ExecuteJava;

public class numberpositivenegativezero {

	public static void main(String[] args) {
		// Verify that number is positive or negative or zero
		
		int n = -9;
		
		if(n>0) {
			System.out.println("Number is POSITIVE " + n);
		}
		else if(n<0) {
			System.out.println("Number is NEGITIVE " + n);
		}
		else {
			System.out.println("Number is Zero " + n);
		}
		
	}

}
